DROP TABLE DZJ_MASTER;
DROP TABLE DZJ_CATEGORY;
DROP TABLE DZJ_USER_INFO;
CREATE TYPE TEXT AS VARCHAR(1000000);
CREATE TABLE DZJ_MASTER (
  DZJ_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  DZJ_CATEGORY_ID INTEGER,
  DZJ_TITLE_TEXT VARCHAR(50),
  DZJ_TEXT TEXT
);
CREATE TABLE DZJ_CATEGORY (
  DZJ_CATEGORY_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  DZJ_CATEGORY_TEXT VARCHAR(50)
);
CREATE TABLE DZJ_USER_INFO (
  USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  USERNAME VARCHAR(50),
  PASSWORD VARCHAR(50),
);
