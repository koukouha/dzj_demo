DROP TABLE dzj_txt_mt;
DROP TABLE dzj_manage_user;
CREATE TYPE TEXT AS VARCHAR(1000000);
CREATE TABLE dzj_txt_mt (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  dzjTitle VARCHAR(50),
  dzjText TEXT
);
CREATE TABLE dzj_manage_user (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
  username VARCHAR(50),
  password VARCHAR(50),
);
